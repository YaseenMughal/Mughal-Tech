class Product {
  final int id;
  final String image;
  final String name;
  final String price;
  final String description;
  final String brand;
  int quantity;

  Product({
    required this.id,
    required this.image,
    required this.name,
    required this.price,
    required this.description,
    required this.brand,
    required this.quantity,
  });
}
